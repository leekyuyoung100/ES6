// 삼항 연산자는 조건에 따라 다른 값을 반환하는 간단한 조건문입니다.
// 형식: 조건 ? 참일 때 값 : 거짓일 때 값

// 예제: 숫자가 짝수인지 홀수인지 확인
const number = 10;

// 삼항 연산자를 사용하여 짝수인지 홀수인지 판단
const result = (number % 2 === 0) ? '짝수' : '홀수';

// 결과 출력
console.log(`${number}은(는) ${result}입니다.`);

// 위 코드에서 (number % 2 === 0)이 조건이고,
// 조건이 참일 경우 '짝수'가 반환되며, 거짓일 경우 '홀수'가 반환됩니다.